    private void jListaPersonaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListaPersonaMousePressed
        JTable tabla = (JTable) evt.getSource();
        Point point = evt.getPoint();
        int row = tabla.rowAtPoint(point);
        if(evt.getClickCount() == 1){
            NombrePro.setText(jListaPersona.getValueAt(jListaPersona.getSelectedRow(), 2).toString());
            RutPro.setText(jListaPersona.getValueAt(jListaPersona.getSelectedRow(), 1).toString());
            PaternoPro.setText(jListaPersona.getValueAt(jListaPersona.getSelectedRow(), 3).toString());
            MaternoPro.setText(jListaPersona.getValueAt(jListaPersona.getSelectedRow(), 4).toString());
            EmailPro.setText(jListaPersona.getValueAt(jListaPersona.getSelectedRow(), 6).toString());
            DirPro.setText(jListaPersona.getValueAt(jListaPersona.getSelectedRow(), 5).toString());
            TelPro.setText(String.valueOf(jListaPersona.getValueAt(jListaPersona.getSelectedRow(), 7).toString()));
            jid.setText(String.valueOf(jListaPersona.getValueAt(jListaPersona.getSelectedRow(), 0).toString()));
        }
    }//GEN-LAST:event_jListaPersonaMousePressed


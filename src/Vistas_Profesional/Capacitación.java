    private void llenarEmpresas() {

        ArrayList<Empresa> listaEmpresas = fpro.getEmpresa();

        cbxEmpresas.removeAllItems();

        for (int i = 0; i < listaEmpresas.size(); i++) {
            cbxEmpresas.addItem(new Empresa(listaEmpresas.get(i).getId_empresa(), listaEmpresas.get(i).getNombre_Empresa(), listaEmpresas.get(i).getId_Representante()));
        }

    }

    private void llenarProfesionales() {

        ArrayList<Persona> listaPersonas = fpro.getPersona();

        cbxProfesionales.removeAllItems();

        for (int i = 0; i < listaPersonas.size(); i++) {
            cbxProfesionales.addItem(new Persona(listaPersonas.get(i).getId_persona(), listaPersonas.get(i).getNombre()));
        }

    }


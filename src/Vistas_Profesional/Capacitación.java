    public void enter(){
        if(jFecha.getText().length() == 0){
           JOptionPane.showMessageDialog(null, "Seleccione una fecha");
       }else if(jAsistentes.getText().length() == 0){
           JOptionPane.showMessageDialog(null, "Ingrese Cantidad de Asistentes");
       }else if(jTema.getText().length() == 0){
           JOptionPane.showMessageDialog(null, "Ingrese Tema a Tratar");
       }else{
           fpro.GuardarCapacitacion(this);
           fpro.listarCapacitaciones(this);
       }
    }
    
